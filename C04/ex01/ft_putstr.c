#include <unistd.h>

void	ft_putstr(char *str)
{
	int		i;
	char	c;

	if (str != 0x0)
	{
		i = 0;
		while (str[i] != '\0')
		{
			c = str[i];
			write(1, &c, 1);
			i++;
		}
	}
}
