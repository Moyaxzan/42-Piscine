int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	diff;

	diff = 0;
	i = 0;
	while (s2[i] != '\0' && s1[i] != '\0' && diff == 0)
	{
		diff = s1[i] - s2[i];
		i++;
	}
	if (diff == 0)
		diff = s1[i] - s2[i];
	return (diff);
}
